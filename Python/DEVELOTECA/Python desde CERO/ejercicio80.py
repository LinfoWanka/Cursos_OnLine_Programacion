# Manipulacion de Diccionarios

casas={"calle":89,"numero":43,"Año de creacion":1989,"Pais":"Mexico"}

print(casas) #// {'calle': 89, 'numero': 43, 'Año de creacion': 1989, 'Pais': 'Mexico'}
print(casas["calle"]) #// 89
casas["calle"]=100
print(casas) #// {'calle': 100, 'numero': 43, 'Año de creacion': 1989, 'Pais': 'Mexico'}
