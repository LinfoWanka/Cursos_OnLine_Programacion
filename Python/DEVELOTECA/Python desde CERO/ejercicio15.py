# Operadores logicos, que nos va a permitir valores de entrada

# variables de tipo booleano, son valores .T. o .F.  (verdad o falso)

a=True
b=False

# Operadores
# and &
print(a and b) # a Y b son Verdad, retorna TRUE si son iguales
print( a & b) # and = &
# or
print(a or b) # retorna FALSE si los dos son falsos
print (a | b)
# not
print(not a) # niega el valor, si a es TRUE devuelve FALSE

