# casting de variable
# identificar cuales son el tipo de datos y podemos convertirlo

# estas conversiones se llaman casting
x= str(5) # este numero lo podemos convertir en un string (texto) lo podemos manejar como txt o #
y= int(3) # este valor se convirtio a entero
z= float(4) # este valor lo  estamos convertiendo a un flotante

print(x)  # 5
print(y)  # 3
print(z)  # 4.0

# Existen funciones que  nos permite convertir estos datos, un dato puede ser de diferentes forma
# siempre en cuando lo convertimos (casting)