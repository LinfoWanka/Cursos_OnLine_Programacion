# Diccionarios en Python (Set o Coleccion)

frutas={"Manzana","Pera","Uva"}
numero={1,5,4,6,2}
valores={True,False,True}
valoresVariados={"Manzana",2,True,20}

print(frutas) #// {'Pera', 'Uva', 'Manzana'}
print(numero) #// {1, 2, 4, 5, 6}
print(valores) #// {False, True}
print(valoresVariados) #// {True, 2, 'Manzana', 20}