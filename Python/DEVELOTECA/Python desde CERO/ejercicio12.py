# Operadores aritmeticos

# Probando las operaciones aritmeticas

print("Deme un valor 1")
a= int(input()) # convertimos en un valor entero
print("Deme un valor 2")
b=int(input())

# operaciones basicas

suma=a+b # ejemplo Suma
resta=a-b # ejemplo Resta
multiplicacion = a*b # ejemplo Multiplicacion
division=a/b # ejemplo Dividir
modulo=a%b # residuo da un valor entero que existe entre dos numero (resta entre los numero 5/4=1.25 y el residuo es 5-4= 1)
exponente=a**b # exponenciacion
floor = a//b  # residuo divisorio

print(suma)
print(resta)
print(multiplicacion)
print(division)
print(modulo)
print(exponente)
print(floor)

