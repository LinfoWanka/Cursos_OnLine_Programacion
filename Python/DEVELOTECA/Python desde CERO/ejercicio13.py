# Operadores de comparacion
# operaciones comparativas "==", "!=" operadores de comparacion

x=3
y=5

# comparacion igual
print(x==y) # si son iguales me devuelve un Verdadero sino Falso
# comparacion diferente (no igual)
print(x!=y) # si x es diferente de y
#  mayor y menor
print(x>y)
print(x<y)
# Mayor o igual y Menor igual
print(x>=y)
print(x<=y)