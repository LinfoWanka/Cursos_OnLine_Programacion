# Ordenamiento reverso con listas

marcasCoches=["Mazda","Ford","Chevrolet","Volkswagen"]
print(marcasCoches)
marcasCoches.sort()
print(marcasCoches)
marcasCoches.sort(reverse=True)
print(marcasCoches)

marcasCoches.sort()
print(marcasCoches)
marcasCoches.reverse() # es igual a  ".sort(reverse=True)"
print(marcasCoches)



