# Ordenamiento de listas

marcasCoches=["Mazda","Ford","Chevrolet","Volkswagen"] # Lista de palabras (string)
print(marcasCoches)
marcasCoches.sort() # ordenar nuestra lista
print(marcasCoches)

numeros=[1,45,2,17,134,123,0,543,5] # Lista de numeros
print(numeros)
numeros.sort()
print(numeros)

datos=[17,134,123,0,"Mazda","Ford","Chevrolet"] # lista con datos direferentes (numeros y cadenas)
print(datos)
datos.sort() # no podemos ordenar datos mezclador ERROR
print(datos) 

